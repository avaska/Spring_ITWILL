package neighborComm;

public class thunderUserDTO {
	private int thunderJoinNum;
	private int thunderNum;
	private int moimNum;
	private String userId;
	private String userNickname;
	private String userPhoto;
	
		
	public int getThunderJoinNum() {
		return thunderJoinNum;
	}
	public void setThunderJoinNum(int thunderJoinNum) {
		this.thunderJoinNum = thunderJoinNum;
	}
	public int getThunderNum() {
		return thunderNum;
	}
	public void setThunderNum(int thunderNum) {
		this.thunderNum = thunderNum;
	}
	public int getMoimNum() {
		return moimNum;
	}
	public void setMoimNum(int moimNum) {
		this.moimNum = moimNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserNickname() {
		return userNickname;
	}
	public void setUserNickname(String userNickname) {
		this.userNickname = userNickname;
	}
	public String getUserPhoto() {
		return userPhoto;
	}
	public void setUserPhoto(String userPhoto) {
		this.userPhoto = userPhoto;
	}

	

}
